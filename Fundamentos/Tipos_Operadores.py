# ADIÇÃO
print(2 + 4)

# SUBTRAÇAO
print(8 - 4)

# MULTIPLICAÇÃO
print(2 * 4)

# DIVISÃO
print(8 / 4)

# DIVISÃO INTEIRA (RETORNA APENAS O NÚMERO INTEIRO DA DIVISÃO)
print(9 // 4)
# MODULO

print(10 % 3)

# EXPONENCIAÇÃO (POTÊNCIA)
print(2 ** 3)

# MAIOR QUE / MAIOR IGUAL TAMBÉM SERVE PARA MENOR QUE / MENOR IGUALA QUE
saldo = 500
saque = 250
print(saldo > saque)
print(saldo >= saque)

# DIFERENTE
print(saldo != saque)

# ATRIBUIÇÃO
print(saldo)
saldo += 300
print(saldo)

# OPERADOR LÓGICO 'AND'
saldo = 1000
saque = 400
limite = 300
saldo >= saque and saque <= limite #FALSO

# OPERADOR LÓGICO 'OR'
saldo >= saque and saque <= limite #VERDADEIRO

